package seller.db;

public class SellerBean {
	
	private String seller_id;
	private String seller_pass;
	private String seller_name;
	private String seller_jumin;
	private String seller_phone;
	private String seller_email;
	private String seller_bs_no;
	
	
	public String getSeller_id() {
		return seller_id;
	}
	public void setSeller_id(String seller_id) {
		this.seller_id = seller_id;
	}
	public String getSeller_pass() {
		return seller_pass;
	}
	public void setSeller_pass(String seller_pass) {
		this.seller_pass = seller_pass;
	}
	public String getSeller_name() {
		return seller_name;
	}
	public void setSeller_name(String seller_name) {
		this.seller_name = seller_name;
	}
	public String getSeller_jumin() {
		return seller_jumin;
	}
	public void setSeller_jumin(String seller_jumin) {
		this.seller_jumin = seller_jumin;
	}
	public String getSeller_phone() {
		return seller_phone;
	}
	public void setSeller_phone(String seller_phone) {
		this.seller_phone = seller_phone;
	}
	public String getSeller_email() {
		return seller_email;
	}
	public void setSeller_email(String seller_email) {
		this.seller_email = seller_email;
	}
	public String getSeller_bs_no() {
		return seller_bs_no;
	}
	public void setSeller_bs_no(String seller_bs_no) {
		this.seller_bs_no = seller_bs_no;
	}
}
