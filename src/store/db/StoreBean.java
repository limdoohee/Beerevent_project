package store.db;


public class StoreBean {

	private int store_no ;
	private String store_register_no ;
	private String store_name ;
	private String store_location ;
	private String store_menu ;
	private String store_dayofweek ;
	private String store_tel ;
	private String store_file ;
	private String seller_id ;
	
	
	public int getStore_no() {
		return store_no;
	}
	public void setStore_no(int store_no) {
		this.store_no = store_no;
	}
	
	public String getStore_register_no() {
		return store_register_no;
	}
	public void setStore_register_no(String store_register_no) {
		this.store_register_no = store_register_no;
	}
	public String getStore_name() {
		return store_name;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	public String getStore_location() {
		return store_location;
	}
	public void setStore_location(String store_location) {
		this.store_location = store_location;
	}
	public String getStore_menu() {
		return store_menu;
	}
	public void setStore_menu(String store_menu) {
		this.store_menu = store_menu;
	}
	public String getStore_dayofweek() {
		return store_dayofweek;
	}
	public void setStore_dayofweek(String store_dayofweek) {
		this.store_dayofweek = store_dayofweek;
	}
	
	public String getStore_tel() {
		return store_tel;
	}
	public void setStore_tel(String store_tel) {
		this.store_tel = store_tel;
	}
	public String getStore_file() {
		return store_file;
	}
	public void setStore_file(String store_file) {
		this.store_file = store_file;
	}
	public String getSeller_id() {
		return seller_id;
	}
	public void setSeller_id(String seller_id) {
		this.seller_id = seller_id;
	}
	
}
